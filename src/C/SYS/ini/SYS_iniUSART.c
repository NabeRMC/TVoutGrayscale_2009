/*----------------------------------------------------------------------------
* Function : Initialize Port
* Descr	   : 
* Inputs   : 
* Outputs  : 
* Return   : 
*---------------------------------------------------------------------------*/
void SYS_iniUSART( void )
{
	UBRR0	= 0;
//	XCK0_DDR |= (1<<XCK0);
	UCSR0C = (1<<UMSEL01)|(1<<UMSEL00)|(0<<UCPHA0)|(0<<UCPOL0);
	UCSR0B = (1<<RXEN0)|(1<<TXEN0);
	UBRR0 = INI_UBRR0;
}
