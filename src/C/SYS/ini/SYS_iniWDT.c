/*----------------------------------------------------------------------------
* Function : Initialize Port
* Descr	   : 
* Inputs   : 
* Outputs  : 
* Return   : 
*---------------------------------------------------------------------------*/
void SYS_iniWDT( void )
{
	asm volatile("wdr");			/* __watchdog_reset();	*/
	MCUSR &= ~(1<<WDRF);
	WDTCSR |= (1<<WDCE)|(1<<WDE);
	WDTCSR = 0x00;
}
